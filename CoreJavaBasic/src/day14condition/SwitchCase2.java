package day14condition;

public class SwitchCase2 {

	public static void main(String[] args) {
		short value = 10;
		switch (value) {
		case 1 :
			System.out.println(" he is shubham");
		
			break;
		case 2 :
			System.out.println( "he is saurabh");
			break;
		case 3 :
			System.out.println(" he is mayur");
			break;
		case 4 :
			System.out.println(" she is bhumi");
			break;
		case 5 :
			System.out.println(" she is Ashu");
			break;
			default :
		   System.out.println(" invalid data");
		}
		
		

	}

}
