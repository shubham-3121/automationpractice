package day14condition;

public class SwitchCase3 {

	public static void main(String[] args) {
		int ascci='b';
		switch (ascci) {
		case 'A':
			System.out.println("The Ascci Value of A is : "+ascci);
			break;
		case 'D':
			System.out.println("The Ascci Value of D is : "+ascci);
			break;
		case 'S':
			System.out.println("The Ascci Value of S is : "+ascci);
			break;
		default :
			System.out.println("the Ascci Value is invalid");
			break;
		}

	}

}
